package inheritance.example;

public class ImportStatement {
    public static void main(String[] args) {
        System.out.println(Math.max(5, 6));        // 6
        System.out.println(Math.min(5, 6));        // 5
        System.out.println(Math.PI);                   // 3.141592653589793
        System.out.println(Math.sqrt(25));           // 5.0 (double)
        System.out.println(Math.cbrt(8));            // 2.0 (double)
        System.out.println(Math.pow(2, 3));        // 8.0
        System.out.println(Math.abs(-25.6));           // 25.6
        System.out.println(Math.ceil(8.6));          // 9.0
        System.out.println(Math.floor(8.6));         // 8.0
        System.out.println(Math.sin(0));             // 0.0
        System.out.println(Math.cos(0));             // 1.0
        System.out.println(Math.tan(0));             // 0.0
        System.out.println(Math.toDegrees(45)); // 2578.3100780887044
        System.out.println(Math.toRadians(45)); // 0.7853981633974483
        System.out.println(Math.log(4.5));           // 1.5040773967762742
        System.out.println(Math.log10(4.5));         // 0.6532125137753437
        System.out.println(Math.random() * 100);       // randomValues
    }
    
}
